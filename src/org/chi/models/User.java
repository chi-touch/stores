package org.chi.models;

public class User {

    private Long id;
    private Long walletId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getWalletId() {
        return walletId;
    }

    public void setWalletId(Long wallet) {
        this.walletId = wallet;
    }



}
